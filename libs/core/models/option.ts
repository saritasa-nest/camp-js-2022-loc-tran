/** Define the data in one sort option. */
export interface SortOption {

  /** Title or name of the option. */
  title: string;

  /** Value of the option. */
  value: string;

}

/** Define the data in one order option. */
export interface OrderOption {

  /** Title or name of the option. */
  title: string;

  /** Value of the option. */
  value: string;

}

/** Define the data in one filter option. */
export interface FilterOption {

  /** Title or name of the filter. */
  title: string;

  /** Value of the filter. */
  value: string;

}
